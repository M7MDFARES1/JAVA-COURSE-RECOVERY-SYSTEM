    private void btnGeneratePDFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGeneratePDFActionPerformed
                                              
    Student s = getSelectedStudent();
    if (s == null) {
        JOptionPane.showMessageDialog(this, "No student selected!", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    try {
        // Create folder if missing
        String folder = "reports/";
        java.io.File dir = new java.io.File(folder);
        if (!dir.exists()) dir.mkdirs();

        // File name
        String filename = folder + "Report_" + s.getStudentId() + ".pdf";

        // Generate PDF
        new AcademicReportGenerator().generate(s, filename);

        JOptionPane.showMessageDialog(this,
                "PDF generated successfully:\n" + filename,
                "Success",
                JOptionPane.INFORMATION_MESSAGE
        );

    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this,
                "Failed to generate PDF.",
                "Error",
                JOptionPane.ERROR_MESSAGE
        );
        ex.printStackTrace();
    }


    }//GEN-LAST:event_btnGeneratePDFActionPerformed

